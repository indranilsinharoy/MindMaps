<node TEXT="Real-Time" ID="ID_581470110" CREATED="1351897427205" MODIFIED="1351897427205"><richcontent TYPE="NOTE">

<html xmlns="http://www.w3.org/1999/xhtml">
<p>Rendering for interactive media, such as games and 
simulations, is calculated and displayed in real time, 
at rates of approximately 20 to 120 frames per 
second. In real-time rendering, the goal is to show as 
much information as possible as the eye can process 
in a 30th of a second (or one frame, in the case of 30 
frame-per-second animation). The goal here is 
primarily speed and not photo-realism. In fact, here 
exploitations are made in the way the eye 'perceives' 
the world, and as a result the final image presented is 
not necessarily that of the real- world, but one close 
enough for the human eye to tolerate. Rendering 
software may simulate such visual effects as lens 
flares, depth of field or motion blur.</p>
<p/>
</html>
