<html xmlns="http://www.w3.org/1999/xhtml">
<p>If a surface exhibits Lambertian reflectance, light falling on it is scattered such that the apparent 
brightness of the surface to an observer is the same regardless of the observer's angle of view. 
More technically, the surface luminance is isotropic.</p>
