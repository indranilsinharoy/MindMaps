<node TEXT="Heightmap/Heightfield" ID="ID_417533675" CREATED="1351897427219" MODIFIED="1351897427219"><richcontent TYPE="NOTE">

<html xmlns="http://www.w3.org/1999/xhtml">
<p>In computer graphics, a heightmap or heightfield is a raster image used to store values, such as 
surface elevation data, for display in 3D computer graphics. A heightmap can be used in bump 
mapping to calculate where this 3D data would create shadow in a material, in displacement 
mapping to displace the actual geometric position of points over the textured surface, or for terrain 
where the heightmap is converted into a 3D mesh.</p>
<p>A heightmap contains one channel interpreted as a distance of displacement or &#x201c;height&#x201d; from the 
&#x201c;floor&#x201d; of a surface and sometimes visualized as luma of a grayscale image, with black representing 
minimum height and white representing maximum height. When the map is rendered, the designer 
can specify the amount of displacement for each unit of the height channel, which corresponds to 
the &#x201c;contrast&#x201d; of the image. Heightmaps can be stored by themselves in existing grayscale image 
formats, with or without specialized metadata, or in specialized file formats such as Daylon 
Leveller, GenesisIV and Terragen documents.</p>
