normal is oriented precisely halfway between the direction 
of incoming light and the direction of the viewer; this is 
called the half-angle direction because it bisects (divides 
into halves) the angle between the incoming light and the 
viewer. Thus, a <span>specularly reflecting surface would show a 
specular highlight as the perfectly sharp reflected image of 
a light source. However, many shiny objects show blurred 
specular highlights.</span>
<span/>
</p>
<p>This can be explained by the existence of <span>microfacets. 
We assume that surfaces that are not perfectly smooth 
are composed of many very tiny facets, each of which is a 
perfect specular reflector. These microfacets have normals 
that are distributed about the normal of the approximating 
smooth surface. The degree to which microfacet normals 
differ from the smooth surface normal is determined by the 
roughness of the surface.</span>
<span/>
</p>
<p>The reason for blurred specular highlights is now clear. At 
points on the object where the smooth normal is close to 
the half-angle direction, many of the <span>microfacets point in 
the half-angle direction and so the specular highlight is 
bright. As one moves away from the center of the 
highlight, the smooth normal and the half-angle direction 
get farther apart; the number of microfacets oriented in the 
half-angle direction falls, and so the intensity of the 
highlight falls off to zero.</span>
<span/>
</p>
</html>
</richcontent>
