<node TEXT="Specular reflection" ID="ID_1255870153" CREATED="1351897427210" MODIFIED="1351897427210"><richcontent TYPE="NOTE">

<html xmlns="http://www.w3.org/1999/xhtml">
<p>A <span>specular highlight is the bright spot of light that appears 
on shiny objects when illuminated (for example, see 
image at right). </span>
<span/>
</p>
<p>Specular highlights are important in 3D computer 
graphics, as <span style=";background:#ffff00">they provide a strong visual cue for the shape 
of an object and its location with respect to light sources 
</span>
<span>in the scene.</span>
<span/>
</p>
<p>The term <span>specular means that light is perfectly reflected in 
a mirror-like way from the light source to the viewer. 
</span>
<span style=";background:#00ff00">Specular reflection is visible only where the surface 
normal is oriented precisely halfway between the direction 
of incoming light and the direction of the viewer</span>
<span>; this is 
called the half-angle direction because it bisects (divides 
into halves) the angle between the incoming light and the 
viewer. Thus, a specularly reflecting surface would show a 
specular highlight as the perfectly sharp reflected image of 
a light source.</span>
<span/>
</p>
<p>
<span style=";background:#ffff99">The specular highlight often reflects the color of the light 
source, not the color of the reflecting object.</span>
<span> This is 
because many materials have a thin layer of clear material 
above the surface of the pigmented material. For example 
plastic is made up of tiny beads of color suspended in a 
clear polymer and human skin often has a thin layer of oil 
or sweat above the pigmented cells. Such materials will 
show specular highlights in which all parts of the color 
spectrum are reflected equally. On metallic materials 
such as gold the color of the specular highlight will reflect 
the color of the material.</span>
<span/>
</p>
<p/>
</html>
</richcontent>
