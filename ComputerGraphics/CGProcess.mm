<html xmlns="http://www.w3.org/1999/xhtml">
<p>Rendering is the process of generating an image from a 
model, by means of computer programs. The model is a 
description of three dimensional objects in a strictly 
defined language or data structure. It would contain 
geometry, viewpoint, texture, lighting, and shading 
information. The image is a digital image or raster 
graphics image. The term may be by analogy with an 
"artist's rendering" of a scene. 'Rendering' is also used to 
describe the process of calculating effects in a video 
editing file to produce final video output.</p>
<p>The process of altering the scene into a suitable form for 
rendering also involves 3D projection which allows a three- 
dimensional image to be viewed in two dimensions.</p>
