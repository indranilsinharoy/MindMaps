<node TEXT="Diffuse interreflection" ID="ID_308428150" CREATED="1351897427210" MODIFIED="1351897427210"><richcontent TYPE="NOTE">

<html xmlns="http://www.w3.org/1999/xhtml">
<p>
<span>Diffuse interreflection is a process whereby light reflected 
from an object strikes other objects in the surrounding 
area, illuminating them. Diffuse interreflection specifically 
describes light reflected from objects which are not shiny 
or specular. In real life terms what this means is that light 
is reflected off non-shiny surfaces such as the ground, 
walls, or fabric, to reach areas not directly in view of a 
light source. If the diffuse surface is colored, the reflected 
light is also colored, resulting in similar coloration of 
surrounding objects.</span>
